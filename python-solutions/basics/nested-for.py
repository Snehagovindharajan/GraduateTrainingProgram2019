# pattern 1
for i in range(1,11):
    for j in range(1,10):
        print(j, end=" ")
    print("\n")

# pattern 2 to print numbers
for i in range(1,11):
    for j in range(1,i):
        print(j, end=" ")
    print("\n")

# pattern 3 to print *
for i in range(1,11):
    for j in range(1,i):
        print('*', end=" ")
    print("\n")

